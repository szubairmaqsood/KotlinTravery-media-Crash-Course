/*
Arithmetic operations are used + , - , *, /
$ {x+y} for evaluation
 */
fun arthmaticOperations(){
    val myVal1 = 99;
    val myVal2 = 7;

    println("Adding result is:: ${myVal1 + myVal2}");
    println("Subtraction  result is:: ${myVal1 - myVal2}");
    println("Multiplication  result is:: ${myVal1 * myVal2}");
    println("Division  result is:: ${myVal1 / myVal2}");

    var x = 3;
    var y = 4;
    var z =5;

    println("The value of x is $x");
    println("The value of x+y is ${x+y}");
    println("The value of x+y+z is ${x+y+z}");


}